#!/usr/bin/env node
// build-chord-shapes.js
// Fetches tombatossals/chords-db (MIT) guitar dataset and bakes a curated subset
// into ../chord-shapes-data.js. Run once when refreshing the curated DB.
//
//   node scripts/build-chord-shapes.js
//
// Source: https://cdn.jsdelivr.net/npm/@tombatossals/chords-db/lib/guitar.json

const fs = require('fs');
const path = require('path');
const https = require('https');

const SRC = 'https://cdn.jsdelivr.net/npm/@tombatossals/chords-db/lib/guitar.json';
const OUT = path.join(__dirname, '..', 'chord-shapes-data.js');

// Their object keys spell sharps as "Csharp" / "Fsharp" (filename-safe) and use
// flats elsewhere; we use sharps everywhere internally.
const KEY_NORMALIZE = {
  'Csharp': 'C#', 'Dsharp': 'D#', 'Fsharp': 'F#', 'Gsharp': 'G#', 'Asharp': 'A#',
  'Eb': 'D#', 'Ab': 'G#', 'Bb': 'A#', 'Db': 'C#', 'Gb': 'F#',
};
const norm = k => KEY_NORMALIZE[k] || k;

// Map chords-db suffixes → our chord-name suffixes.
// Slash chords ('/E', 'm/G', …) handled separately.
const SUFFIX_MAP = {
  'major':  '',
  'minor':  'm',
  'dim':    'dim',
  'dim7':   'dim7',
  'aug':    'aug',
  'sus2':   'sus2',
  'sus4':   'sus4',
  '6':      '6',
  '7':      '7',
  '9':      '9',
  '11':     '11',
  '13':     '13',
  'maj7':   'maj7',
  'maj9':   'maj9',
  'm6':     'm6',
  'm7':     'm7',
  'm7b5':   'm7b5',
  'm9':     'm9',
  'add9':   'add9',
  'madd9':  'madd9',
  '7sus4':  '7sus4',
};

function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https.get(url, res => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        return fetchJson(res.headers.location).then(resolve, reject);
      }
      let body = '';
      res.on('data', chunk => body += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(body)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

// Convert a chords-db position to absolute-fret form matching our existing CHORD_SHAPES convention.
function convertPosition(pos) {
  const baseFret = pos.baseFret || 1;
  const positions = pos.frets.map((rel, i) => {
    if (rel === -1) return null; // muted — omit from positions array
    const absolute = rel === 0 ? 0 : (baseFret + rel - 1);
    return { string: i, fret: absolute, finger: (pos.fingers && pos.fingers[i]) || 0 };
  }).filter(Boolean);
  return { baseFret, positions };
}

function shapeKey(rootKey, chordsDbSuffix) {
  if (chordsDbSuffix.startsWith('/')) {
    const bass = norm(chordsDbSuffix.slice(1));
    return rootKey + '/' + bass;
  }
  if (chordsDbSuffix.startsWith('m/')) {
    const bass = norm(chordsDbSuffix.slice(2));
    return rootKey + 'm/' + bass;
  }
  const ourSuffix = SUFFIX_MAP[chordsDbSuffix];
  if (ourSuffix === undefined) return null; // unsupported suffix — skip
  return rootKey + ourSuffix;
}

function bassNoteFor(suffix, rootKey) {
  if (suffix.startsWith('/'))   return norm(suffix.slice(1));
  if (suffix.startsWith('m/'))  return norm(suffix.slice(2));
  return rootKey; // root in bass
}

(async () => {
  console.log('Fetching', SRC);
  const db = await fetchJson(SRC);
  console.log(`Loaded ${db.main.numberOfChords} chord positions across ${db.keys.length} keys × ${db.suffixes.length} suffixes`);

  const out = {};
  let totalShapes = 0;
  let skippedSuffix = 0;

  Object.entries(db.chords).forEach(([rawKey, entries]) => {
    const root = norm(rawKey);
    entries.forEach(entry => {
      const key = shapeKey(root, entry.suffix);
      if (key === null) { skippedSuffix++; return; }
      if (!entry.positions || entry.positions.length === 0) return;
      // First position only — chords-db lists the most idiomatic voicing first.
      const conv = convertPosition(entry.positions[0]);
      const bass = bassNoteFor(entry.suffix, root);
      out[key] = { ...conv, bassNote: bass };
      totalShapes++;
    });
  });

  console.log(`Wrote ${totalShapes} shapes (skipped ${skippedSuffix} entries with unsupported suffixes)`);

  const json = JSON.stringify(out, null, 1);
  const file = [
    '// chord-shapes-data.js',
    '// Curated guitar chord shapes for standard tuning (E A D G B E).',
    '// Source: tombatossals/chords-db (MIT). Generated by scripts/build-chord-shapes.js.',
    '// Each entry: { positions: [{string, fret, finger}], baseFret, bassNote }',
    '// String index 0 = low E, 5 = high E. Frets are absolute (subtract baseFret for diagram offset).',
    '',
    `window.CHORD_SHAPES_DB = ${json};`,
    '',
  ].join('\n');

  fs.writeFileSync(OUT, file);
  console.log('Wrote', OUT, `(${file.length} bytes)`);
})();
